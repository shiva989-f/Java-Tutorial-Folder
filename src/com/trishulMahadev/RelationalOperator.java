package com.trishulMahadev;

public class RelationalOperator {
    public static void main(String[] args) {
        int a = 10;
        int b = 20;

        System.out.println(a > b); // 10 is greater than 20
        System.out.println(a < b); // 10 is less than 20
        System.out.println(a <= b); // 10 is less or equal to 20
        System.out.println(a >= b); // 10 is greater or equal to 20
        System.out.println(a != b); // 10 is not equal to 20
        System.out.println(a == b); // 10 is equal to 20

    }
}
